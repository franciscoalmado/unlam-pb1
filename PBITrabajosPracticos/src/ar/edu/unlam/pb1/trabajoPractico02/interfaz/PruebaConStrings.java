package ar.edu.unlam.pb1.trabajoPractico02.interfaz;

public class PruebaConStrings {

	public static void main(String[] args) {
		
		String minuscula = "java es case sensitive";
		
		System.out.println("Cantidad de caracteres de la variable minúscula: "+minuscula.length());
		
		String mayuscula = "JAVA ES CASE SENSITIVE.";
		
		System.out.println("Cantidad de caracteres de la variable mayúscula: "+mayuscula.length());		
	}
}